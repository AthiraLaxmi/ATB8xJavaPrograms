package sept.ex_16092024_Operators;

public class Lab031_Arthmetic_Operator {
    public static void main(String[] args) {
        //Arithmentic_operator
        // + , - , * , /
        int a = 10;
        int b = 2;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);

        // one more arithmetic operator is:
        // % - modulus

        System.out.println(a % b);

       // 2| 10 | 5 -- Quoient
        // | 10 |
      // ---------------
       //    0 - Remainder

        // another eg:

        System.out.println(67 % 13);

       // 13 |67| 5
        //   |65|
       //  ----------
         //   2
    }
}
