package sept.ex_16092024_Operators;

public class Lab037_Relational_Operator {
    public static void main(String[] args) {
        // Relational Operator = boolean
        // > <,>= <=, == !=

        int a=30;
        int b=10;

        boolean c= a>b;
        System.out.println(c);
        boolean d= a<b;
        System.out.println(d);
        boolean e= a!=b;
        System.out.println(e);
    }
}
