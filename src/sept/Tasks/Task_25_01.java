package sept.Tasks;

import java.util.Scanner;

public class Task_25_01 {
    public static void main(String[] args) {
        // Table of num n=10; print the table with for or while

        int n = 10;
        System.out.println(n);

        for (int i = 1; i <= 10; i++) {
            System.out.println(n + "*" + i + "=" + (n * i));
        }
    }
}

