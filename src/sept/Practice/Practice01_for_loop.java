package sept.Practice;

public class Practice01_for_loop {
    public static void main(String[] args) {
        // Table of num n=9; print the table with for loop

        int n = 9;

        System.out.println(n);

        for (int i=1; i<=10; i++){
            System.out.println(n + "*" + i +"="+ (i*9));

        }


    }


}
