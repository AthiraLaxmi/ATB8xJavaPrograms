package sept.ex_25092024_Loop;

public class Lab096_While_Loop {
    public static void main(String[] args) {
        //while loop
       // same concept as for loop
        //A,B,C - initilization, condition, Increment/ Decrement
        // the main diff is initialization will do outside the loop

        int i=0;
        while (i <= 10){
            System.out.println(i);
            i++;
        }

    }
}
